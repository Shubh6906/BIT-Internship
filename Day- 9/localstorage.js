// Local storage is used to store the data from domain to local device.

localStorage.setItem('name', 'Shubh'); // It will set the given data to the local storage

localStorage; // It will show the stored data in the console

localStorage.clear(); // It will clear the local storage of that domain

localStorage.getItem('name'); // It will output "Shubh" in the console.
// i.e., it will fetch the name and print in the console.

localStorage.removeItem('name'); // It will remove the name & its value