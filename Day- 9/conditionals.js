// Conditionals in JavaScript

var age = 4;
// Single IF Statement
if(age > 18){
    console.log("You can drink...");
}

// IF-ELSE Statement
if(age > 18){
    console.log("You can drink...");
}
else{
    console.log("You cannot drink...");
}

// IF-ELSE Ladder
if(age > 18){
    console.log("You can drink...");
}
else if(age = 17){
    console.log("Wait for just 1 year more...");
}
else{
    console.log("You cannot drink...");
}